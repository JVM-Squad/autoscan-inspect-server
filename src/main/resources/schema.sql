CREATE TABLE IF NOT EXISTS E_MAIN_REQ(
    ID_MAIN_REQ VARCHAR(255) PRIMARY KEY,
    VA_NAME VARCHAR(255) NOT NULL,
    VA_USR VARCHAR(50),
    DH_DBT TIMESTAMP(3) NOT NULL,
    DH_FIN TIMESTAMP(3) NOT NULL,
    LNCH VARCHAR(255) NOT NULL,
    LOC VARCHAR(255),
    VA_CMPLT CHAR NOT NULL,
    VA_THRED VARCHAR(255) NOT NULL,
    VA_APP_NME VARCHAR(255) NOT NULL,
    VA_VRS VARCHAR(255),
    VA_ADRS VARCHAR(255) NOT NULL,
    VA_ENV VARCHAR(255),
    VA_OS VARCHAR(255) NOT NULL,
    VA_RE VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS E_IN_REQ (
    ID_IN_REQ VARCHAR(36) PRIMARY KEY,
    VA_MTH VARCHAR(10) NOT NULL,
    VA_PRTCL VARCHAR(255) NOT NULL,
    VA_HST VARCHAR(255) NOT NULL,
    CD_PRT INT,
    VA_PTH VARCHAR(255) NOT NULL,
    VA_QRY VARCHAR(1000),
    VA_CNT_TYP VARCHAR(255),
    VA_AUTH VARCHAR(255),
    CD_STT INT,
    VA_I_SZE BIGINT,
    VA_O_SZE BIGINT,
    DH_DBT TIMESTAMP(3) NOT NULL,
    DH_FIN TIMESTAMP(3) NOT NULL,
    VA_THRED VARCHAR(255) NOT NULL,
    VA_API_NME VARCHAR(255) NOT NULL,
    VA_USR VARCHAR(255),
    VA_APP_NME VARCHAR(255) NOT NULL,
    VA_VRS VARCHAR(255),
    VA_ADRS VARCHAR(255) NOT NULL,
    VA_ENV VARCHAR(255),
    VA_OS VARCHAR(255) NOT NULL,
    VA_RE VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS E_OUT_REQ (
    ID_OUT_REQ VARCHAR(36) PRIMARY KEY,
    VA_MTH VARCHAR(10) NOT NULL,
    VA_PRTCL VARCHAR(255) NOT NULL,
    VA_HST VARCHAR(255) NOT NULL,
    CD_PRT INT,
    VA_PTH VARCHAR(255) NOT NULL,
    VA_QRY VARCHAR(1000),
    VA_CNT_TYP VARCHAR(255),
    VA_AUTH VARCHAR(255),
    CD_STT INT,
    VA_I_SZE BIGINT,
    VA_O_SZE BIGINT,
    DH_DBT TIMESTAMP(3) NOT NULL,
    DH_FIN TIMESTAMP(3) NOT NULL,
    VA_THRED VARCHAR(255) NOT NULL,
    CD_IN_REQ VARCHAR(36) NOT NULL
);

CREATE TABLE IF NOT EXISTS E_OUT_QRY (
    ID_OUT_QRY BIGINT,
    VA_HST VARCHAR(255),
    CD_PRT INT,
    VA_SCHMA VARCHAR(255),
    DH_DBT TIMESTAMP(3) NOT NULL,
    DH_FIN TIMESTAMP(3), -- nullable
    VA_USR VARCHAR(255),
    VA_THRED VARCHAR(255) NOT NULL,
    VA_DRV VARCHAR(255),
    VA_DB_NME VARCHAR(255),
    VA_DB_VRS VARCHAR(255),
    VA_CMPLT CHAR NOT NULL,
    CD_IN_REQ VARCHAR(36) NOT NULL
);

CREATE TABLE IF NOT EXISTS E_DB_ACT(
    VA_TYP VARCHAR(255) NOT NULL,
    DH_DBT TIMESTAMP(3) NOT NULL,
    DH_FIN TIMESTAMP(3), -- nullable
    VA_CMPLT  CHAR  NOT NULL,
    CD_OUT_QRY BIGINT NOT NULL
);
